  log = { level = 'warn' }, -- The default print log level. One of: "trace", "debug", "info", "warn", "error", "fatal".
  profile = {
    enable = true,
    threshold = 0.2, -- integer in milliseconds, plugins which load faster than this won't be shown in profile output
  }
